/* This program demonstrates the use of if-else statements in Java. It checks whether a given number is even or odd and prints the result to the console. */
public class IfElseExample{
	public static void main(String[] args){
		int number = 5;
		if (number % 2 == 0)
 			System.out.println(number + " is even.");
		else
			 System.out.println(number + " is odd.");
	}
}